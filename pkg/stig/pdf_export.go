	pdf.CellFormat(0, 8, fmt.Sprintf("Report Date: %s", r.ScanDate.Format("2006-01-02 15:04:05 MST")), "", 1, "C", false, 0, "")
	pdf.CellFormat(0, 8, fmt.Sprintf("Scan Duration: %s", r.ScanDuration.String()), "", 1, "C", false, 0, "")
	pdf.Ln(10)

	// System info
	pdf.SetFont("Helvetica", "B", 12)
	pdf.CellFormat(0, 8, "Target System:", "", 1, "C", false, 0, "")
	pdf.SetFont("Helvetica", "", 11)
	pdf.CellFormat(0, 6, fmt.Sprintf("Hostname: %s", r.Hostname), "", 1, "C", false, 0, "")
	pdf.CellFormat(0, 6, fmt.Sprintf("OS: %s", r.OSVersion), "", 1, "C", false, 0, "")
