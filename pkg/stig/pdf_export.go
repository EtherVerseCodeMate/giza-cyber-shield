func (r *ComprehensiveReport) writeExecutiveSummaryPDF(pdf *fpdf.Fpdf) {
	pdf.SetFont("Helvetica", "B", 16)
	pdf.CellFormat(0, 10, "EXECUTIVE SUMMARY", "B", 1, "L", false, 0, "")
	pdf.Ln(5)

	// Compliance Score Box
	pdf.SetFillColor(240, 240, 240)
	pdf.SetFont("Helvetica", "B", 14)
	gradeColor := getGradeColor(r.ExecutiveSummary.ComplianceGrade)
	pdf.SetTextColor(gradeColor[0], gradeColor[1], gradeColor[2])
	pdf.CellFormat(90, 15, fmt.Sprintf("Overall Compliance: %.1f%% [%s]",
		r.ExecutiveSummary.OverallCompliance,
		r.ExecutiveSummary.ComplianceGrade), "1", 0, "C", true, 0, "")
	pdf.SetTextColor(0, 0, 0)

	riskColor := getRiskColor(r.ExecutiveSummary.OverallRisk)
	pdf.SetTextColor(riskColor[0], riskColor[1], riskColor[2])
	pdf.CellFormat(90, 15, fmt.Sprintf("Risk Level: %s", r.ExecutiveSummary.OverallRisk), "1", 1, "C", true, 0, "")
	pdf.SetTextColor(0, 0, 0)
	pdf.Ln(5)

	// Framework Scores
	pdf.SetFont("Helvetica", "B", 12)
	pdf.CellFormat(0, 8, "Framework Compliance Scores:", "", 1, "L", false, 0, "")
	pdf.SetFont("Helvetica", "", 10)

	frameworks := []struct {
		name  string
		score float64
	}{
		{"RHEL-09-STIG", r.ExecutiveSummary.STIGCompliance},
		{"CIS Benchmark", r.ExecutiveSummary.CISCompliance},
		{"NIST 800-53", r.ExecutiveSummary.NIST80053Compliance},
		{"NIST 800-171", r.ExecutiveSummary.NIST800171Compliance},
		{"CMMC 3.0 L3", r.ExecutiveSummary.CMMCCompliance},
	}

	for _, fw := range frameworks {
		drawProgressBar(pdf, fw.name, fw.score)
	}
	pdf.Ln(5)

	// Critical Findings
	pdf.SetFont("Helvetica", "B", 12)
	pdf.CellFormat(0, 8, "Critical Findings Summary:", "", 1, "L", false, 0, "")
	pdf.SetFont("Helvetica", "", 10)

	pdf.SetTextColor(200, 0, 0)
	pdf.CellFormat(60, 6, fmt.Sprintf("CAT I/Critical: %d", r.ExecutiveSummary.CAT1Findings), "", 0, "L", false, 0, "")
	pdf.SetTextColor(200, 100, 0)
	pdf.CellFormat(60, 6, fmt.Sprintf("CAT II/High: %d", r.ExecutiveSummary.CAT2Findings), "", 0, "L", false, 0, "")
	pdf.SetTextColor(100, 100, 0)
	pdf.CellFormat(60, 6, fmt.Sprintf("CAT III/Medium: %d", r.ExecutiveSummary.CAT3Findings), "", 1, "L", false, 0, "")
	pdf.SetTextColor(0, 0, 0)
	pdf.Ln(5)

	// Top Risks
	if len(r.ExecutiveSummary.TopRisks) > 0 {
		pdf.SetFont("Helvetica", "B", 12)
		pdf.CellFormat(0, 8, "Top Risks Requiring Attention:", "", 1, "L", false, 0, "")
		pdf.SetFont("Helvetica", "", 10)
		for i, risk := range r.ExecutiveSummary.TopRisks {
			pdf.CellFormat(0, 6, fmt.Sprintf("%d. %s", i+1, risk), "", 1, "L", false, 0, "")
		}
	}
}

func (r *ComprehensiveReport) writeFrameworkDetailsPDF(pdf *fpdf.Fpdf) {
	pdf.SetFont("Helvetica", "B", 16)
	pdf.CellFormat(0, 10, "FRAMEWORK VALIDATION DETAILS", "B", 1, "L", false, 0, "")
	pdf.Ln(5)

	for frameworkName, result := range r.Results {
		pdf.SetFont("Helvetica", "B", 12)
		pdf.CellFormat(0, 8, fmt.Sprintf("%s (v%s)", frameworkName, result.Version), "", 1, "L", false, 0, "")
		pdf.SetFont("Helvetica", "", 10)
		pdf.CellFormat(45, 6, fmt.Sprintf("Total Controls: %d", result.TotalControls), "", 0, "L", false, 0, "")
		pdf.CellFormat(45, 6, fmt.Sprintf("Passed: %d", result.Passed), "", 0, "L", false, 0, "")
		pdf.CellFormat(45, 6, fmt.Sprintf("Failed: %d", result.Failed), "", 0, "L", false, 0, "")
		pdf.CellFormat(45, 6, fmt.Sprintf("Score: %.1f%%", result.ComplianceScore()), "", 1, "L", false, 0, "")
		pdf.Ln(3)
	}
}

func (r *ComprehensiveReport) writeCriticalFindingsPDF(pdf *fpdf.Fpdf) {
	pdf.SetFont("Helvetica", "B", 16)
	pdf.SetTextColor(200, 0, 0)
	pdf.CellFormat(0, 10, "CRITICAL FINDINGS (CAT I) - IMMEDIATE ACTION REQUIRED", "B", 1, "L", false, 0, "")
	pdf.SetTextColor(0, 0, 0)
	pdf.Ln(5)

	count := 0
	for _, result := range r.Results {
		for _, finding := range result.Findings {
			if finding.Status == "Fail" && (finding.Severity == SeverityCAT1 || finding.Severity == SeverityCritical) {
				count++
				pdf.SetFont("Helvetica", "B", 11)
				pdf.CellFormat(0, 7, fmt.Sprintf("%d. [%s] %s", count, finding.ID, finding.Title), "", 1, "L", false, 0, "")
				pdf.SetFont("Helvetica", "", 9)
				pdf.CellFormat(0, 5, fmt.Sprintf("Severity: %s", finding.Severity), "", 1, "L", false, 0, "")
				pdf.CellFormat(0, 5, fmt.Sprintf("Remediation: %s", truncate(finding.Remediation, 100)), "", 1, "L", false, 0, "")
				pdf.Ln(3)
			}
		}
	}
}

