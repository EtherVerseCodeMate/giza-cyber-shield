// ExportToPDF exports comprehensive validation results to PDF format
// Executive Intelligence Brief format for DoD leadership
func (r *ComprehensiveReport) ExportToPDF(outputPath string) error {
	pdf := fpdf.New("P", "mm", "A4", "")
	pdf.SetMargins(15, 15, 15)
	pdf.SetAutoPageBreak(true, 15)

	// Title Page
	pdf.AddPage()
	r.writeTitlePage(pdf)

	// Executive Summary
	pdf.AddPage()
	r.writeExecutiveSummaryPDF(pdf)

	// Framework Details
	pdf.AddPage()
	r.writeFrameworkDetailsPDF(pdf)

	// Critical Findings
	if r.ExecutiveSummary.CAT1Findings > 0 {
		pdf.AddPage()
		r.writeCriticalFindingsPDF(pdf)
	}

	// POA&M Summary
	if len(r.POAMItems) > 0 {
		pdf.AddPage()
		r.writePOAMSummaryPDF(pdf)
	}

	// Save PDF
	pdfPath := outputPath + ".pdf"
	if err := pdf.OutputFileAndClose(pdfPath); err != nil {
		// Fallback to text export if PDF fails
		return r.exportToTextFallback(outputPath)
	}

	return nil
}

